package com.skp.opx.mss.util;

import android.content.Context;
import android.widget.Toast;

public class ToastUtil {
	public static void showToastById(Context context, int resId) {
		Toast toast = Toast.makeText(context, context.getString(resId), Toast.LENGTH_SHORT);
		toast.show();
	}
	
	public static void showToastByString(Context context, String text) {
		Toast toast = Toast.makeText(context, text, Toast.LENGTH_SHORT);
		toast.show();
	}
}
